-- ─── V12: voc_chemical + voc_consumption + feature VOC ───────
CREATE TABLE IF NOT EXISTS voc_chemical (
    id             BIGSERIAL        NOT NULL,
    code           VARCHAR(40)      NOT NULL,
    material_type  VARCHAR(20)      NOT NULL DEFAULT 'SOLVENT',
    classification VARCHAR(40),
    manufacturer   VARCHAR(60),
    voc_factor     DOUBLE PRECISION NOT NULL DEFAULT 0,
    price_per_kg   DOUBLE PRECISION,
    active         BOOLEAN          NOT NULL DEFAULT TRUE,
    created_at     TIMESTAMP(6)     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at     TIMESTAMP(6),
    CONSTRAINT pk_voc_chemical PRIMARY KEY (id),
    CONSTRAINT uq_voc_chemical_code UNIQUE (code)
);

CREATE TABLE IF NOT EXISTS voc_consumption (
    id              BIGSERIAL        NOT NULL,
    version         BIGINT,
    production_date DATE             NOT NULL,
    section         VARCHAR(20)      NOT NULL DEFAULT 'SEW',
    line            VARCHAR(10)      NOT NULL,
    chemical_code   VARCHAR(40)      NOT NULL,
    quantity_kg     DOUBLE PRECISION NOT NULL DEFAULT 0,
    reuse_kg        DOUBLE PRECISION NOT NULL DEFAULT 0,
    created_at      TIMESTAMP(6)     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at      TIMESTAMP(6),
    CONSTRAINT pk_voc_consumption PRIMARY KEY (id),
    CONSTRAINT uq_voc_consumption UNIQUE (production_date, section, line, chemical_code)
);
CREATE INDEX IF NOT EXISTS idx_voc_cons_date          ON voc_consumption (production_date);
CREATE INDEX IF NOT EXISTS idx_voc_cons_date_sec_line ON voc_consumption (production_date, section, line);

INSERT INTO features (feature_key, display_name, url_patterns, http_methods, category, priority)
SELECT 'VOC', 'VOC', '/voc/**,/api/v1/voc/**', NULL, 'Analytics', 500
WHERE NOT EXISTS (SELECT 1 FROM features WHERE feature_key = 'VOC');

INSERT INTO role_features (role_id, feature_id)
SELECT r.id, f.id
FROM roles r
CROSS JOIN features f
WHERE r.name IN ('ROLE_ADMIN', 'ROLE_MANAGER')
  AND f.feature_key = 'VOC'
  AND NOT EXISTS (SELECT 1 FROM role_features rf WHERE rf.role_id = r.id AND rf.feature_id = f.id);

-- ─── V13: voc_standard_rate ──────────────────────────────────
CREATE TABLE IF NOT EXISTS voc_standard_rate (
    id            BIGSERIAL        NOT NULL,
    article_no    VARCHAR(40)      NOT NULL,
    chemical_code VARCHAR(40)      NOT NULL,
    kg_per_pair   DOUBLE PRECISION NOT NULL DEFAULT 0,
    created_at    TIMESTAMP(6)     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at    TIMESTAMP(6),
    CONSTRAINT pk_voc_standard_rate PRIMARY KEY (id),
    CONSTRAINT uq_voc_std_rate UNIQUE (article_no, chemical_code)
