-- ─── Bảng Hibernate-era chưa từng có CREATE migration ────────
-- (new_style_entry / eff_incentive_rate / eff_multiplier được ddl-auto=update tạo
--  trước khi Flyway hoạt động; V3/V4 chỉ ALTER có điều kiện. Tạo ở đây theo shape
--  cuối của entity để DB rỗng chạy đủ chuỗi + prod validate khớp.)
CREATE TABLE IF NOT EXISTS new_style_entry (
    id         BIGSERIAL    NOT NULL,
    data_month VARCHAR(7)   NOT NULL,
    section    VARCHAR(20)  NOT NULL,
    line       VARCHAR(10)  NOT NULL,
    style      VARCHAR(50)  NOT NULL,
    quantity   INTEGER      NOT NULL,
    CONSTRAINT pk_new_style_entry PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS eff_incentive_rate (
    id          BIGSERIAL        NOT NULL,
    sec         VARCHAR(10)      NOT NULL,
    section     VARCHAR(10)      NOT NULL,
    wt          INTEGER          NOT NULL,
    eff_percent DOUBLE PRECISION NOT NULL,
    rate        DOUBLE PRECISION NOT NULL,
    CONSTRAINT pk_eff_incentive_rate PRIMARY KEY (id),
    CONSTRAINT uq_eff_incentive_rate UNIQUE (sec, eff_percent)
);

CREATE TABLE IF NOT EXISTS eff_multiplier (
    id         BIGSERIAL        NOT NULL,
    sec        VARCHAR(10)      NOT NULL,
    section    VARCHAR(10)      NOT NULL,
    wt         INTEGER          NOT NULL,
    rate_alias VARCHAR(10),
    grade1     DOUBLE PRECISION,
    grade2     DOUBLE PRECISION,
    grade3     DOUBLE PRECISION,
    grade4     DOUBLE PRECISION,
    grade5     DOUBLE PRECISION,
    grade6     DOUBLE PRECISION,
    grade7     DOUBLE PRECISION,
    grade8     DOUBLE PRECISION,
    grade9     DOUBLE PRECISION,
    CONSTRAINT pk_eff_multiplier PRIMARY KEY (id),
    CONSTRAINT uq_eff_multiplier_sec UNIQUE (sec)
);

-- ─── V10: user_line_assignments ─────────────────────────────
CREATE TABLE IF NOT EXISTS user_line_assignments (
    id      BIGINT       GENERATED BY DEFAULT AS IDENTITY,
    user_id BIGINT       NOT NULL,
    section VARCHAR(50)  NOT NULL,
    line    VARCHAR(50)  NOT NULL DEFAULT '',
    CONSTRAINT pk_user_line_assignments PRIMARY KEY (id),
    CONSTRAINT uk_ula_user_section_line UNIQUE (user_id, section, line),
    CONSTRAINT fk_ula_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
);
CREATE INDEX IF NOT EXISTS idx_ula_user ON user_line_assignments (user_id);

