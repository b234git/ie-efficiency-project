    article_no  VARCHAR(40)      NOT NULL,
    model_code  VARCHAR(60),
    model_name  VARCHAR(160),
    base_e      DOUBLE PRECISION,
    base_f      DOUBLE PRECISION,
    created_at  TIMESTAMP(6)     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at  TIMESTAMP(6),
    CONSTRAINT pk_voc_recipe_article PRIMARY KEY (article_no)
);

-- created_at tường minh: bảng do Hibernate tạo (dev) không có DB DEFAULT cho cột này
INSERT INTO voc_recipe_article (article_no, created_at)
SELECT DISTINCT article_no, CURRENT_TIMESTAMP FROM voc_standard_rate
ON CONFLICT (article_no) DO NOTHING;

